/* ── Constants ───────────────────────────────────────────────────────── */

#define MAX_CREDS          128
#define MAX_PATH_LEN       512
#define HTTP_BUF_SZ        8192
#define JSON_VAL_SZ        256
#define MAX_RESTARTS       20
#define RESTART_DELAY_SEC  3
#define BROKER_CHECK_SEC   2
#define LOG_INTERVAL_SEC   60

/* ── Types ───────────────────────────────────────────────────────────── */

typedef struct {
    char   name[JSON_VAL_SZ];
    char   role[JSON_VAL_SZ];
    time_t issued_at;
} CredEntry;

/* ── Globals ─────────────────────────────────────────────────────────── */

static volatile int g_running = 1;
static void on_signal(int sig) { (void)sig; g_running = 0; }

#ifdef COMPAT_WINDOWS
static HANDLE g_broker_handle     = NULL;
static DWORD  g_broker_pid        = 0;
#else
static pid_t  g_broker_pid        = -1;
#endif
static int    g_broker_running    = 0;
static int    g_broker_restarts   = 0;
static time_t g_broker_last_start = 0;

static CredEntry g_creds[MAX_CREDS];
static int       g_cred_count = 0;

static const char *g_agent_name = "herald";
static int         g_broker_port = 1883;
static int         g_api_port    = 7780;
static char        g_config_dir[MAX_PATH_LEN];
static char        g_conf_file[MAX_PATH_LEN + 32];
static char        g_passwd_file[MAX_PATH_LEN + 32];
static char        g_acl_file[MAX_PATH_LEN + 32];
static char        g_mosquitto_bin[MAX_PATH_LEN];
static char        g_mosquitto_passwd_bin[MAX_PATH_LEN];
static int         g_no_auth = 0;

/* ── Config generation ───────────────────────────────────────────────── */

static int write_mosquitto_conf(void)
{
    FILE *f = fopen(g_conf_file, "w");
    if (!f) {
        fprintf(stderr, "[%s] failed to open %s: %s\n",
                g_agent_name, g_conf_file, strerror(errno));
        return -1;
    }

    fprintf(f, "# NML Collective Herald — generated by herald_agent\n");
    fprintf(f, "# Do not edit; changes will be overwritten on restart.\n\n");

    fprintf(f, "listener %d\n", g_broker_port);
    fprintf(f, "protocol mqtt\n\n");

    if (g_no_auth) {
        fprintf(f, "allow_anonymous true\n");
    } else {
        fprintf(f, "allow_anonymous false\n");
        fprintf(f, "password_file %s\n", g_passwd_file);
        fprintf(f, "acl_file %s\n", g_acl_file);
    }

    fprintf(f, "\nretain_available true\n");
    fprintf(f, "persistence false\n\n");

    fprintf(f, "log_type error\n");
    fprintf(f, "log_type warning\n");
    fprintf(f, "log_type notice\n");
    fprintf(f, "log_dest stdout\n\n");

    fprintf(f, "sys_interval 10\n");

    fclose(f);

    printf("[%s] wrote %s (port=%d, auth=%s)\n",
           g_agent_name, g_conf_file, g_broker_port,
           g_no_auth ? "off" : "on");
    return 0;
}

static int write_acl_file(void)
{
    if (g_no_auth) {
        return 0;
    }

    FILE *f = fopen(g_acl_file, "w");
    if (!f) {
        fprintf(stderr, "[%s] failed to open %s: %s\n",
                g_agent_name, g_acl_file, strerror(errno));
        return -1;
    }

    fprintf(f, "# NML Collective ACL — managed by herald_agent\n\n");
    fprintf(f, "topic readwrite nml/#\n\n");

    for (int i = 0; i < g_cred_count; i++) {
        const char *r = g_creds[i].role;
        if (strcmp(r, "sentient") == 0 ||
            strcmp(r, "enforcer") == 0 ||
            strcmp(r, "herald")   == 0) {
            fprintf(f, "user %s\n", g_creds[i].name);
            fprintf(f, "topic readwrite nml/#\n");
            fprintf(f, "topic readwrite $SYS/#\n\n");
        }
    }

    fclose(f);
    return 0;
}

