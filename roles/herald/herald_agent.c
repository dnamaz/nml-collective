/*
 * herald_agent.c — NML Collective Herald.
 *
 * Supervises the MQTT broker (Mosquitto), manages credentials and ACLs,
 * and exposes a minimal HTTP REST API for credential operations.
 *
 * The Herald is pure infrastructure — it does NOT join the mesh as a peer.
 * Every agent in the collective connects through the Herald.
 *
 * Usage:
 *   ./herald_agent [--name NAME] [--broker-port PORT] [--api-port PORT]
 *                  [--config-dir DIR] [--mosquitto PATH]
 *                  [--mosquitto-passwd PATH] [--no-auth]
 *
 * REST API:
 *   POST /credentials/issue   — issue a credential (agent + password + role)
 *   POST /credentials/revoke  — revoke a credential (agent)
 *   GET  /health              — broker and registry status
 */

